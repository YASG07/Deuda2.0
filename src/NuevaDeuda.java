    private void escribirArchivoObj(Deuda Aux){
        try{
            java.io.FileOutputStream FO = new java.io.FileOutputStream("Deuda.obj");
            java.io.ObjectOutputStream FOS = new java.io.ObjectOutputStream(FO);
            FOS.writeObject(Aux);
            FOS.flush();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(NuevaDeuda.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(NuevaDeuda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }// escribir archivo .obj
    
